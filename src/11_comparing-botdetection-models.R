# =============================================================================
# Cross-Language Bot Detection Comparison
# English (enwiki) · Russian (ruwiki) · Swedish (svwiki)
# =============================================================================
# Goal: Run a unified bot-detection pipeline across three Wikipedia language
# editions in a single script, then compare:
#   A. Model performance metrics side-by-side
#   B. Bot prevalence and edit-volume differences
#   C. Which edit-type features are universally vs. language-specifically
#      predictive of bot behaviour
#   D. Comment feature signal strength per language
#   E. Cross-language transfer: how well does a model trained on English
#      generalise to Russian and Swedish without retraining?
#
# Each language is processed through an identical pipeline (helpers below),
# producing a standardised results list that feeds all comparison analyses.
#
# MODEL: Elastic-net logistic regression (alpha = 0.5), user-level 80/20
# stratified split, inverse-frequency class weights, 5-fold CV on AUC.
# =============================================================================

# --- Required libraries (loaded once) ----------------------------------------
library(tidyverse)   # Data wrangling & plotting
library(jsonlite)    # Parsing JSON blobs stored in each edit record
library(glmnet)      # Elastic-net penalised logistic regression
library(lubridate)   # Timestamp parsing for temporal features
library(ggrepel)     # Non-overlapping labels on scatter plots

setwd("")

# Convenience null-coalescing operator (used inside extract_features)
`%||%` <- function(a, b) if (!is.null(a)) a else b


# =============================================================================
# SHARED HELPERS
# =============================================================================

# -----------------------------------------------------------------------------
# extract_features()
#
# Parses one edit's JSON blob into a flat named list of numeric counts:
#   - One counter per (node_type × action) pair from "node-edits"
#     e.g. "mwTemplate_insert", "mwHeading_remove"
#   - Total words inserted / removed from "text-edits"
#   - Total number of node-edits and text-edits
#
# Returns NULL on malformed JSON so map() can skip broken rows gracefully.
# -----------------------------------------------------------------------------
extract_features <- function(json_str) {
  parsed <- tryCatch(fromJSON(json_str, simplifyVector = FALSE),
                     error = function(e) NULL)
  if (is.null(parsed)) return(NULL)
  
  # Count each (node_type, action) pair — e.g. 3 template insertions → 3
  node_feats <- list()
  for (ne in parsed[["node-edits"]]) {
    key <- paste0(ne[[1]], "_", ne[[2]])
    node_feats[[key]] <- (node_feats[[key]] %||% 0) + 1
  }
  
  # Sum inserted and removed word counts separately across all text-edits
  n_word_insert <- 0L
  n_word_remove <- 0L
  for (te in parsed[["text-edits"]]) {
    if (te[[2]] == "insert") n_word_insert <- n_word_insert + as.integer(te[[4]])
    if (te[[2]] == "remove") n_word_remove <- n_word_remove + as.integer(te[[4]])
  }
  
  c(node_feats,
    list(n_word_insert = n_word_insert,
         n_word_remove = n_word_remove,
         n_node_edits  = length(parsed[["node-edits"]]),
         n_text_edits  = length(parsed[["text-edits"]])))
}


# -----------------------------------------------------------------------------
# make_comment_features()
#
# Derives binary and numeric features from the free-text revision comment
# that editors leave when saving an edit. These are language-agnostic keyword
# patterns; language-specific terms (e.g. Cyrillic "бот") would need to be
# added per edition if required.
#
# Returns a data frame with one row per edit (same order as input).
# -----------------------------------------------------------------------------
make_comment_features <- function(dat) {
  dat %>%
    mutate(
      # Numeric: raw length and word count
      comment_length      = nchar(revision_comment),
      comment_is_empty    = as.integer(is.na(revision_comment) | revision_comment == ""),
      comment_nwords      = str_count(revision_comment, "\\S+"),
      
      # Bots often self-identify or describe their tooling in comments
      mentions_bot        = as.integer(str_detect(tolower(revision_comment), "bot")),
      mentions_automated  = as.integer(str_detect(tolower(revision_comment),
                                                  "automated|script|auto")),
      
      # Structural comment patterns common in bot comments
      has_wikilink        = as.integer(str_detect(revision_comment, "\\[\\[")),
      starts_with_bracket = as.integer(str_detect(revision_comment, "^\\[")),
      
      # /* Section */ prefix is generated by clicking "edit section" — human only
      starts_with_slash   = as.integer(str_detect(revision_comment, "^/")),
      
      # Semantic content of the comment
      mentions_fix        = as.integer(str_detect(tolower(revision_comment),
                                                  "fix|correct|clean")),
      mentions_revert     = as.integer(str_detect(tolower(revision_comment),
                                                  "revert|undo|rv\\b")),
      
      # Informal punctuation signals human writing
      has_punctuation     = as.integer(str_detect(revision_comment, "[!?]"))
    ) %>%
    select(starts_with("comment_"), starts_with("mentions_"),
           starts_with("starts_with_"), starts_with("has_"))
}


# -----------------------------------------------------------------------------
# run_pipeline()
#
# End-to-end pipeline for one language edition:
#   1. Load up to n_rows edits from a gzip-compressed JSON file
#   2. Extract edit-type features and comment features
#   3. Perform a user-level stratified 80/20 train/test split
#   4. Fit an elastic-net logistic regression with class-imbalance weights
#   5. Evaluate and return a standardised results list
#
# Arguments:
#   file       — path to the .json.gz file
#   n_rows     — maximum number of rows to read (use fewer for quick tests)
#   lang_label — human-readable label used in plots and tables
#
# Returns a named list with: data, combined features, metrics, coefficients,
# feature signal ratios, fitted model, and feature column names.
# Returns NULL if there are too few bot users to split.
# -----------------------------------------------------------------------------
run_pipeline <- function(file, n_rows, lang_label) {
  cat("\n====================================================\n")
  cat(" Processing:", lang_label, "\n")
  cat("====================================================\n")
  
  # --- 1. Load data ----------------------------------------------------------
  con <- textConnection(readLines(gzfile(file), n = n_rows))
  dat <- stream_in(con, verbose = FALSE)
  close(con)
  cat("Rows loaded:", nrow(dat),
      "| Bot share:", round(mean(dat$is_bot) * 100, 1), "%\n")
  
  # --- 2a. Edit-type features -----------------------------------------------
  # map() applies extract_features() to every JSON string, then bind_rows()
  # stacks the results. Spaces in column names are replaced with underscores
  # so glmnet does not choke on them.
  edit_feats <- dat$edit_types_json %>%
    map(extract_features) %>%
    map(as_tibble) %>%
    bind_rows(.id = "row_id") %>%
    mutate(
      across(everything(), ~ replace_na(.x, 0)),   # Missing counts → 0
      row_id    = as.integer(row_id),
      is_bot    = as.integer(dat$is_bot[row_id]),
      page_id   = dat$page_id[row_id],
      user_text = dat$user_text[row_id]
    ) %>%
    rename_with(~ str_replace_all(., " ", "_"))
  
  # --- 2b. Comment features -------------------------------------------------
  comm_feats <- make_comment_features(dat)
  
  # --- 2c. Combine into one feature matrix ----------------------------------
  combined  <- bind_cols(edit_feats, comm_feats)
  feat_cols <- combined %>%
    select(-row_id, -is_bot, -page_id, -user_text) %>%
    names()
  
  # --- 3. User-level stratified 80/20 split ---------------------------------
  # Each user's edits are kept entirely in either train or test.
  # If a user has any bot-labelled edit, they are treated as a bot user.
  user_labels <- combined %>%
    group_by(user_text) %>%
    summarise(is_bot = max(is_bot), .groups = "drop")
  
  set.seed(42)
  bot_u   <- user_labels %>% filter(is_bot == 1) %>% pull(user_text)
  human_u <- user_labels %>% filter(is_bot == 0) %>% pull(user_text)
  
  # Guard: at least 2 bot users are needed for a meaningful stratified split
  if (length(bot_u) < 2) {
    cat("  Too few bot users — skipping model fit.\n")
    return(NULL)
  }
  
  train_users <- c(sample(bot_u,   size = max(1, floor(0.8 * length(bot_u)))),
                   sample(human_u, size = floor(0.8 * length(human_u))))
  
  tr_idx <- which( combined$user_text %in% train_users)
  te_idx <- which(!combined$user_text %in% train_users)
  
  train <- combined[tr_idx, ]
  test  <- combined[te_idx, ]
  
  # --- 4. Class-imbalance weights -------------------------------------------
  # Rare bot class is upweighted so it contributes equally to the loss.
  n_tr <- nrow(train)
  n_b  <- sum(train$is_bot)
  w_tr <- ifelse(train$is_bot == 1,
                 n_tr / (2 * n_b),
                 n_tr / (2 * (n_tr - n_b)))
  
  X_tr <- train %>% select(all_of(feat_cols)) %>% as.matrix()
  X_te <- test  %>% select(all_of(feat_cols)) %>% as.matrix()
  y_tr <- train$is_bot
  
  # --- 5. Elastic-net (5-fold CV, optimise AUC) -----------------------------
  set.seed(42)
  cv_m <- cv.glmnet(X_tr, y_tr,
                    family       = "binomial",   # Logistic regression
                    alpha        = 0.5,           # 50% L1 + 50% L2
                    weights      = w_tr,
                    nfolds       = 5,
                    type.measure = "auc")
  
  # Predict at lambda that maximises CV-AUC; threshold at 0.5
  prob <- predict(cv_m, newx = X_te, s = "lambda.min", type = "response")[, 1]
  pred <- as.integer(prob > 0.5)
  y_te <- test$is_bot
  
  # Confusion matrix counts
  tp <- sum(pred == 1 & y_te == 1)   # Correct bot predictions
  fp <- sum(pred == 1 & y_te == 0)   # Humans wrongly flagged as bots
  fn <- sum(pred == 0 & y_te == 1)   # Bots the model missed
  tn <- sum(pred == 0 & y_te == 0)   # Correct human predictions
  
  # --- 6. Compile outputs ---------------------------------------------------
  
  # Scalar performance metrics
  metrics <- tibble(
    language  = lang_label,
    n_edits   = nrow(dat),
    bot_share = round(mean(dat$is_bot) * 100, 1),
    n_users   = nrow(user_labels),
    n_bots    = sum(user_labels$is_bot),
    auc_cv    = round(max(cv_m$cvm), 3),
    precision = round(tp / (tp + fp), 3),    # Of predicted bots, how many are real?
    recall    = round(tp / (tp + fn), 3),    # Of all real bots, how many did we catch?
    f1        = round(2 * tp / (2 * tp + fp + fn), 3),   # Harmonic mean
    accuracy  = round((tp + tn) / (tp + tn + fp + fn), 3)
  )
  
  # Non-zero model coefficients (positive = bot signal, negative = human signal)
  cm    <- coef(cv_m, s = "lambda.min")
  coefs <- tibble(
    language = lang_label,
    term     = rownames(cm),
    estimate = as.numeric(cm)
  ) %>%
    filter(term != "(Intercept)")
  
  # Log2(bot mean / human mean) for every feature — used in EDA plots
  mean_ratio <- combined %>%
    group_by(is_bot) %>%
    summarise(across(all_of(feat_cols), mean), .groups = "drop") %>%
    pivot_longer(-is_bot, names_to = "feature", values_to = "mean_val") %>%
    pivot_wider(names_from = is_bot, values_from = mean_val,
                names_prefix = "bot_") %>%
    rename(human = bot_0, bot = bot_1) %>%
    mutate(
      language  = lang_label,
      log2ratio = log2((bot + 0.001) / (human + 0.001))   # +0.001 avoids log(0)
    )
  
  list(
    data      = dat,
    combined  = combined,
    metrics   = metrics,
    coefs     = coefs,
    ratios    = mean_ratio,
    model     = cv_m,
    feat_cols = feat_cols
  )
}


