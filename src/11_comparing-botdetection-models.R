# =============================================================================
# 8. CROSS-LANGUAGE TRANSFER TEST
#
# Trains on English and evaluates directly on Russian and Swedish WITHOUT
# retraining. This answers: "Do the patterns learned from English bots
# generalise to other languages, or is bot behaviour edition-specific?"
#
# To align feature spaces, missing columns are added as zero-filled columns.
# A large gap between transfer F1 and native F1 indicates that bot behaviour
# in that language has unique signals not present in English training data.
# =============================================================================

transfer_results <- tibble()

eng_res <- results[["english"]]
if (!is.null(eng_res)) {
  
  for (lang_key in setdiff(names(results), "english")) {
    tgt     <- results[[lang_key]]
    tgt_lab <- tgt$metrics$language
    
    # Align target feature matrix to the English feature set:
    # add any columns the target is missing (with zeros) and reorder to match
    tgt_X <- tgt$combined %>%
      select(any_of(eng_res$feat_cols)) %>%
      {
        missing_cols <- setdiff(eng_res$feat_cols, names(.))
        for (mc in missing_cols) .[[mc]] <- 0L   # Feature unseen in this edition → 0
        .
      } %>%
      select(all_of(eng_res$feat_cols)) %>%
      as.matrix()
    
    tgt_X[is.na(tgt_X)] <- 0   # Residual NAs → 0
    
    # Apply the English model to the target language's edits
    prob_t <- predict(eng_res$model, newx = tgt_X,
                      s = "lambda.min", type = "response")[, 1]
    pred_t <- as.integer(prob_t > 0.5)
    y_t    <- as.integer(tgt$combined$is_bot)
    
    tp_t <- sum(pred_t == 1 & y_t == 1)
    fp_t <- sum(pred_t == 1 & y_t == 0)
    fn_t <- sum(pred_t == 0 & y_t == 1)
    
    transfer_results <- bind_rows(transfer_results, tibble(
      target      = tgt_lab,
      f1_transfer = round(2 * tp_t / (2 * tp_t + fp_t + fn_t), 3),
      f1_native   = tgt$metrics$f1
    ))
  }
}

cat("\n=== Cross-language transfer vs. native model ===\n")
