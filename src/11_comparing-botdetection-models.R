# =============================================================================
# 6. PAIRWISE COEFFICIENT SCATTER PLOTS
#
# Plotting one language's coefficients against another's shows:
#   - Points near the diagonal: both models agree on direction AND magnitude
#   - Points in Q1 or Q3 (same sign): consistent direction, different strength
#   - Points in Q2 or Q4 (opposite sign): the feature predicts opposite things
#     in different editions — a sign of language-specific behaviour
# =============================================================================

make_pair_scatter <- function(lang_a, lang_b, df_wide) {
  # Guard: both languages must be present in the data
  if (!all(c(lang_a, lang_b) %in% names(df_wide))) return(NULL)
  
  df_wide %>%
    ggplot(aes(x = .data[[lang_a]], y = .data[[lang_b]], label = term)) +
    geom_hline(yintercept = 0, linetype = "dashed", colour = "grey70") +
    geom_vline(xintercept = 0, linetype = "dashed", colour = "grey70") +
    # Dotted diagonal = features with identical coefficient in both languages
    geom_abline(slope = 1, intercept = 0, linetype = "dotted", colour = "grey50") +
    # Colour by whether the two models agree on direction (sign)
    geom_point(aes(colour = sign(.data[[lang_a]]) == sign(.data[[lang_b]])),
               size = 2.5, alpha = 0.8) +
    geom_text_repel(size = 2.8, max.overlaps = 12) +
    scale_colour_manual(
      values = c("TRUE" = "tomato", "FALSE" = "steelblue"),
      labels = c("TRUE" = "Same direction", "FALSE" = "Opposite direction")
    ) +
    labs(title    = paste("Coefficients:", lang_a, "vs.", lang_b),
         subtitle = "Points on dotted diagonal = identical signal strength",
         x        = paste0("Coefficient (", lang_a, ")"),
         y        = paste0("Coefficient (", lang_b, ")"),
         colour   = NULL) +
    theme_minimal(base_size = 12)
}

